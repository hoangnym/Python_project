print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")


def start_game():
    dec_one = input("Do you want to go left or right?\n").strip().lower()

    if dec_one == "right":
        return "Game Over."
    else:
        print("Good choice!")

    dec_two = input(
        "You arrived at the coast. Do you want to wait or swim?\n").strip().lower()

    if dec_two == "swim":
        return "Game Over."
    else:
        print("Good choice!")

    dec_three = input(
        "You arrived at three doors. Which one do you open: red, yellow, or blue?\n").strip().lower()

    if dec_three == "yellow":
        return "You Win!"
    else:
        return "Game Over."


print(start_game())


    
        
